package com.main;

public class Main {
    public static void main(String[] args) {
        // operator assigment
        // +=
        float varible = 525;
        // +=
        varible += 234;
        System.out.println(varible);
        varible -= 234;
        System.out.println(varible);
        varible *= 234;
        System.out.println(varible);
        varible /= 234;
        System.out.println(varible);
        varible %= 234;
        System.out.println(varible);
    }
}
