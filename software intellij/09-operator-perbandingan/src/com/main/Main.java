package com.main;

public class Main {
    public static void main(String[] args) {
        // operator perbandingan
        int angkaPertama = 10;
        int angkaKedua = 12;
        System.out.println(angkaPertama > angkaKedua);
        System.out.println(angkaPertama < angkaKedua);
        System.out.println(angkaPertama >= angkaKedua);
        System.out.println(angkaPertama <= angkaKedua);
        System.out.println(angkaPertama == angkaKedua);
        System.out.println(angkaPertama != angkaKedua);

    }
}
